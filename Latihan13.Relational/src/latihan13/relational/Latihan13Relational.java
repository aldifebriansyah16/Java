public class Latihan13Relational {

    public static void main(String[] args) {
        int bi11 = 10;
        int bi12 = 20;
        System.out.println("a == b = " + (bi11 == bi12));
        System.out.println("a != b = " + (bi11 != bi12));
        System.out.println("a > b = " + (bi11 > bi12));
        System.out.println("a < b = " + (bi11 < bi12));
        System.out.println("b >= a = " + (bi12 >= bi11));
        System.out.println("b <= a = " + (bi12 <= bi11));
    }   
}
